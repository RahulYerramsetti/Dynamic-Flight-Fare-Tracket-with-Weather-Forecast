public class Astronomy {
    
    private String sunrise;

  public String getSunrise() { return this.sunrise; }

  public void setSunrise(String sunrise) { this.sunrise = sunrise; }

  private String sunset;

  public String getSunset() { return this.sunset; }

  public void setSunset(String sunset) { this.sunset = sunset; }

  private String moonrise;

  public String getMoonrise() { return this.moonrise; }

  public void setMoonrise(String moonrise) { this.moonrise = moonrise; }

  private String moonset;

  public String getMoonset() { return this.moonset; }

  public void setMoonset(String moonset) { this.moonset = moonset; }
}
